package cwiczenie2;

public class Person {

    private String firstName;
    private String lastName;
    private String city;
    private String street;
    private int streetNumber;

    public Person(String firstName, String lastName, String city, String street, int streetNumber) {
        this.firstName = firstName;
        this.lastName = lastName;
        this.city = city;
        this.street = street;
        this.streetNumber = streetNumber;
    }
}

